
// Switch

let color = 'red';

switch (color) {

    case 'red':
        console.log('the color is red');
        break;

    case 'blue':
        console.log('the sky is blue');
        break;

    case 'brown':
        console.log('the bears are brown');
        break;

    default:
        console.log('color is pink');
}

